package com.github.argon.sos.mod.sdk.booster;

public enum BoostMode {
    /**
     * The boost will be added to the original booster
     */
    ADD,

    /**
     * The boost will be multiplied with the original booster
     */
    MULTI,

    /**
     * The boost will be a percentage of the original booster
     */
    PERCENT
}
