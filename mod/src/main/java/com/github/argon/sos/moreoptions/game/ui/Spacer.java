package com.github.argon.sos.moreoptions.game.ui;

/**
 * For adding space between other UI elements
 */
public class Spacer extends AbstractRender {

    public Spacer() {
        super();
    }

    public Spacer(int size) {
        super(size, size);
    }

    public Spacer(int width, int height) {
        super(width, height);
    }
}
